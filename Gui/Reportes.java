    private void btnInvertirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInvertirActionPerformed
        
        for (Celebracion c : datos.obtenerRegistros()){
            String Pais = c.getPais();
            c.setPais(logica.invertirRec(Pais));
        }
        
        paisesInvertidos = !paisesInvertidos;
        
        cargarTabla(logica.obtenerTodas());
        lblEvento.setText(paisesInvertidos ? "Paises Ivertidos" : "Paises Restaurados");
    }//GEN-LAST:event_btnInvertirActionPerformed

