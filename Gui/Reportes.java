    private void cargarCamposDesdeTabla(){
        
        int fila = tblReporte.getSelectedRow();

        if (fila < 0) return;

        txtId.setText(String.valueOf(tblReporte.getValueAt(fila, 0)));
        txtDescripcion.setText(String.valueOf(tblReporte.getValueAt(fila, 2)));
        txtBuscarPais.setText(String.valueOf(tblReporte.getValueAt(fila, 3)));

        // Validación para evitar crash por tipo de dato
        Object fechaObj = tblReporte.getValueAt(fila, 1);

        if (fechaObj instanceof java.time.LocalDate fecha) {

            java.util.Calendar cal = java.util.Calendar.getInstance();
            cal.clear(); //evita resuduos de fechas en la memoria
            cal.set(fecha.getYear(), fecha.getMonthValue() - 1, fecha.getDayOfMonth());
            Calendario.setCalendar(cal);

        } else {
            Calendario.setDate(new java.util.Date());
        }
    }
    
    private void configurarTabla(){
        
        //Tomado de la IA para el bloqueo de las celldas
        javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel(
        new Object[]{"ID", "Fecha", "Descripción", "País"}, 0
        ) {
        @Override
        public boolean isCellEditable(int row, int column) {
            return false; //bloquea todas las celdas
        }
     };

        tblReporte.setModel(modelo);
    }
    
    private void cargarTabla(List<Entidad.Celebracion> lista){
        
        javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tblReporte.getModel();
        modelo.setRowCount(0);
        
        for (Entidad.Celebracion c : lista) {
        modelo.addRow(new Object[]{
            c.getId(),c.getFecha(),c.getDescripcion(),c.getPais()
        });
        
        lblEvento.setText("Mostrando " + lista.size() + " evento(s)");
        }
        
    }

